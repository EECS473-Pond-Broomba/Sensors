void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim){
	if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_1)  // if the interrupt source is channel2
		{
			if (Is_First_Captured==0) // if the first value is not captured
			{
				input_capture1 = __HAL_TIM_GetCompare(&htim4, TIM_CHANNEL_1); // read the first value
				Is_First_Captured = 1;  // set the first captured as true
				// Now change the polarity to falling edge
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim4, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_FALLING);
			}

			else if (Is_First_Captured==1)   // if the first is already captured
			{
				input_capture2 = __HAL_TIM_GetCompare(&htim4, TIM_CHANNEL_1);  // read second value
				__HAL_TIM_SET_COUNTER(&htim4, 0);  // reset the counter

				if (input_capture2 > input_capture1)
				{
					Difference = (input_capture2-input_capture1)/58;
				}

				Is_First_Captured = 0; // set it back to false

				// set polarity to rising edge
				__HAL_TIM_SET_CAPTUREPOLARITY(&htim4, TIM_CHANNEL_1, TIM_INPUTCHANNELPOLARITY_RISING);

			}
		}
}
